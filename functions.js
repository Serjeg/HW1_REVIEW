/*1. Get the string name of the day of the week by day number */

function weekday(days) {
	let dayName = ['Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday', 'September', 'Sunday'];
	switch (days) {
		case 1: {
			return dayName [0];
		}
		case 2: {
			return dayName [1];
		}
		case 3: {
			return dayName [2];
		}
		case 4: {
			return dayName [3];
		}
		case 5: {
			return dayName [4];
		}
		case 6: {
			return dayName [5];
		} 
		case 7: {
			return dayName [6];
		}
		break;
	}
}
console.log(weekday(3));


